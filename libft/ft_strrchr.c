#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	pos_null;

	i = 0;
	while (s[i])
		i++;
	pos_null = i;
	if (c == 0)
		return ((char *)(s + pos_null));
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	return (NULL);
}
